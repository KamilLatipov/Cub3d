#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned char	*arr;
	size_t			i;

	arr = s;
	i = 0;
	while (n != 0)
	{
		arr[i] = c;
		n--;
		i++;
	}
	return (s);
}
